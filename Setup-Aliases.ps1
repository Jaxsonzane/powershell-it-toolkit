function galmanager      { & "$scriptsPath\GALManager.ps1" }
function mapdrives       { & "$scriptsPath\MapDrives.ps1" }
function removechrome    { & "$scriptsPath\RemoveChrome.ps1" }
function reinstallchrome { & "$scriptsPath\ReinstallChrome.ps1" }

function scripts {
    Write-Host "" 
    Write-Host "=== IT Toolkit Commands ===" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "  MORNING ROUTINE" -ForegroundColor Yellow
    Write-Host "  morning          - Daily reset and health check" -ForegroundColor White
    Write-Host ""
    Write-Host "  PERFORMANCE" -ForegroundColor Yellow
    Write-Host "  speedboost       - Clear temp files, flush DNS, high performance mode" -ForegroundColor White
    Write-Host "  diskcleanup      - Deep disk cleanup" -ForegroundColor White
    Write-Host ""
    Write-Host "  NETWORK" -ForegroundColor Yellow
    Write-Host "  netdiag          - Full network diagnostic" -ForegroundColor White
    Write-Host "  netreset         - Reset full network stack" -ForegroundColor White
    Write-Host "  netmonitor       - Live connection monitor" -ForegroundColor White
    Write-Host "  wifireport       - Wi-Fi health check" -ForegroundColor White
    Write-Host "  proxychecker     - Proxy and DNS audit" -ForegroundColor White
    Write-Host ""
    Write-Host "  VPN" -ForegroundColor Yellow
    Write-Host "  vpnrepair        - Cisco Secure Client diagnostic and fix" -ForegroundColor White
    Write-Host ""
    Write-Host "  PRINTERS" -ForegroundColor Yellow
    Write-Host "  printerdiag      - Printer status check" -ForegroundColor White
    Write-Host "  spoolerreset     - Clear stuck queue and restart spooler" -ForegroundColor White
    Write-Host ""
    Write-Host "  APPS" -ForegroundColor Yellow
    Write-Host "  outlookfix       - Outlook cache clear and repair" -ForegroundColor White
    Write-Host "  slackfix         - Slack cache clear and repair" -ForegroundColor White
    Write-Host "  browserfix       - Browser repair (Chrome/Edge/Firefox)" -ForegroundColor White
    Write-Host ""
    Write-Host "  SYSTEM" -ForegroundColor Yellow
    Write-Host "  updatefix        - Fix stuck Windows Updates" -ForegroundColor White
    Write-Host "  crashdiag        - App crash and Event Log diagnostic" -ForegroundColor White
    Write-Host "  profilediag      - User profile corruption check" -ForegroundColor White
    Write-Host "  rdpfix           - Enable and fix Remote Desktop" -ForegroundColor White
    Write-Host ""
    Write-Host "  EXCHANGE / GAL" -ForegroundColor Yellow
    Write-Host "  galmanager       - Hide/show/update users in Global Address List" -ForegroundColor White
    Write-Host ""
    Write-Host "  DRIVES" -ForegroundColor Yellow
    Write-Host "  mapdrives        - Map network drives" -ForegroundColor White
    Write-Host ""
    Write-Host "  CHROME" -ForegroundColor Yellow
    Write-Host "  removechrome     - Completely remove Chrome" -ForegroundColor White
    Write-Host "  reinstallchrome  - Reinstall Chrome keeping user data" -ForegroundColor White
    Write-Host ""
    Write-Host "  Type 'scripts' anytime to see this list." -ForegroundColor Gray
    Write-Host ""
}
"@

$profileDir = Split-Path $PROFILE -Parent
if (-not (Test-Path $profileDir)) {
    New-Item -ItemType Directory -Path $profileDir -Force | Out-Null
}

Set-Content -Path $PROFILE -Value $profileContent -Encoding UTF8
Write-Host "Profile written to: $PROFILE" -ForegroundColor Green

. $PROFILE
Write-Host "Aliases loaded and ready!" -ForegroundColor Green
Write-Host "Type 'scripts' to see all available commands." -ForegroundColor Cyan
Write-Host "`n=== Setup Complete ===" -ForegroundColor Cyan
