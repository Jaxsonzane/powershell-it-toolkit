# ============================================================
#  Setup-Aliases.ps1 - PowerShell Profile Alias Setup
#  Run as Administrator ONCE to configure all script shortcuts
#
#  UPDATE $scriptsPath below to wherever you saved the scripts
# ============================================================

Write-Host "`n=== Setting up PowerShell Aliases ===" -ForegroundColor Cyan

# UPDATE THIS PATH before running
$scriptsPath = "C:\Users\YOUR_USERNAME\Documents\PowerShell Scripts"

if (-not (Test-Path $scriptsPath)) {
    Write-Host "ERROR: Scripts folder not found at: $scriptsPath" -ForegroundColor Red
    Write-Host "Update the path at the top of this script and re-run." -ForegroundColor Yellow
    exit
}

Write-Host "Clearing existing profile and rewriting clean..." -ForegroundColor Yellow

$profileContent = @"
# ============================================================
# IT Toolkit Script Aliases
# Generated by Setup-Aliases.ps1 — re-run to update
# ============================================================

function morning         { & "$scriptsPath\MorningStartup.ps1" }
function speedboost      { & "$scriptsPath\SpeedBoost.ps1" }
function netdiag         { & "$scriptsPath\NetDiag.ps1" }
function netreset        { & "$scriptsPath\NetReset.ps1" }
function netmonitor      { & "$scriptsPath\NetMonitor.ps1" }
function wifireport      { & "$scriptsPath\WiFiReport.ps1" }
function proxychecker    { & "$scriptsPath\ProxyChecker.ps1" }
function vpnrepair       { & "$scriptsPath\VPNRepair.ps1" }
function printerdiag     { & "$scriptsPath\PrinterDiag.ps1" }
